const express = require('express')
const router = express.Router()
const BarberoController = require('../controllers/barbero.controller')
const validateRequest = require('../middlewares/validateRequest')
const authMiddleware = require('../middlewares/auth.middleware')
const { BarberoSchema } = require('../../../infraestructure/schemas/barbero.schema')

router.post('/', validateRequest({ body: BarberoSchema }), BarberoController.crear)
router.get('/', BarberoController.obtenerTodos)
router.get('/especialidad/:especialidad', BarberoController.buscarPorEspecialidad)
router.get('/email/:email', BarberoController.obtenerPorEmail)
router.get('/:id', BarberoController.obtenerPorId)
router.get('/:id/stats', BarberoController.obtenerStats)
router.get('/:id/citas', BarberoController.obtenerCitas)
router.get('/:id/servicios', BarberoController.obtenerServicios)
router.post('/:id/servicios', authMiddleware, BarberoController.agregarServicio)
router.delete('/:id/servicios/:id_servicio', authMiddleware, BarberoController.eliminarServicio)
router.get('/:id/disponibilidad', BarberoController.obtenerDisponibilidad)
router.get('/:id/disponibilidad/:fecha', BarberoController.obtenerDisponibilidad)
router.put('/:id', authMiddleware, validateRequest({ body: BarberoSchema.partial() }), BarberoController.actualizar)
router.put('/:id/horario', authMiddleware, BarberoController.actualizarHorario)
router.delete('/:id', authMiddleware, BarberoController.eliminar)
router.post('/:id/foto', authMiddleware, BarberoController.actualizarFoto)

module.exports = router